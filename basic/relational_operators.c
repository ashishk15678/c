#include <stdio.h>

int main()
{
    int a = 2, b = 3;

    printf("\n\t\tRelational Operators in C\n\n");

    printf("a is %d and b is %d , a==b = %d\n", a, b, a == b);
    printf("a is %d and b is %d , a>b = %d\n", a, b, a > b);
    printf("a is %d and b is %d , a<b = %d\n", a, b, a < b);
    printf("a is %d and b is %d , a>=b = %d\n", a, b, a >= b);
    printf("a is %d and b is %d , a<=b = %d\n", a, b, a <= b);
    printf("a is %d and b is %d , a!=b = %d\n", a, b, a != b);
}